    public DefaultListModel limpiarJList(){
        DefaultListModel modelo= new DefaultListModel();
        jList1.setModel(modelo);
        return modelo;
        
    }
    
    public DefaultListModel agregarDato(){
        String nombre,apellidos,direccion,telefono;
        nombre=txtNombre.getText();
        apellidos=txtApellidos.getText();
        direccion=txtDireccion.getText();
        telefono=txtTelefono.getText();
        ListaPersonas lista= new ListaPersonas();
        Persona p= new Persona(nombre,apellidos,telefono,direccion);
        lista.agregarPersona(p);
        String elemento=nombre+"--"+apellidos+"--"+telefono+"--"+direccion;
        DefaultListModel modelo= (DefaultListModel) jList1.getModel();
        modelo.addElement(elemento);
        
        return modelo;
        
    }
    
    public DefaultListModel borrarDato(){
        ListaPersonas lista= new ListaPersonas();
        DefaultListModel modelo= (DefaultListModel) jList1.getModel();
        
        modelo.remove(jList1.getSelectedIndex());

        return modelo;
    }
    

